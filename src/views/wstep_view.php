						<h1>Wspinaczka górska – moje hobby</h1>
					</header>
					<div class="tresc">
						<p>W otoczeniu fantastycznych górskich krajobrazów codzienność szybko odchodzi w zapomnienie. Podczas długiego wejścia na szczyt możesz chłonąć przyrodę gór pełną piersią, a także sprawdzić swoje możliwości, zarówno fizyczne jak i psychiczne. Wyjaśnimy, czym jest alpinizm oraz jakimi predyspozycjami musisz dysponować, żeby uprawiać sporty górskie.  </p>
						<img src="static/img/pic2.jpg" alt="image">
						<h2 id="warunki">Warunki i predyspozycje do wspinaczki</h2>
						<p>
							Granice między górskim trekkingiem i wspinaczką są płynne. Kryterium rozróżniającym nie mogą być ani odległości w kilometrach, ani przekraczane wysokości. Jednakże wspinaczka górska cechuje się tym, że pokonujesz zarówno <b>bezdroża jak i pionowe odcinki wspinaczkowe</b>, żeby dotrzeć do celu.
							<br><br>
							Wspinaczka w Alpach jest często nazywana <b>alpinizmem</b>, w Tatrach natomiast taternictwem i obejmuje wędrówki, badanie terenów górskich oraz przewodnictwo w tatrach. Pojęcie „wspinaczka górska“ jest często używane jako <b>pojęcie nadrzędne dla różnych sportów górskich</b>. W zależności od stopnia trudności i różnicy wysokości wspinając się pokonujesz na swej drodze <b>następujące tereny:</b>
						</p>
						<ul class="kolo_zdj">
							<li>Szlaki górskie (o stopniu trudności od średniego do trudnego)</li>
							<li>Tereny płaskie na lodowcach</li>
							<li>Piargi (strome zbocza pokryte okruchami i odłamkami skalnymi)</li>
							<li>Odcinki wspinaczkowe na skałach</li>
							<li>Żelazne percie (via ferrata)</li>
						</ul>
						<p>
							Jeżeli twój szlak wiedzie na wysokość <b>ponad 3000 metrów</b>, to alpiniści mówią już o <b>wyprawie wysokogórskiej</b>. To koronna dyscyplina wspinaczkowa. Ponieważ granica lodowca przebiega na wysokości około 3500 metrów, na wyprawie wysokogórskiej poruszasz się więc zawsze po pokrytym lodem i pełnym szczelin terenie.
							<br><br>
							Żeby czuć się pewnie jako alpinista w górskim osamotnieniu, potrzebna jest <b>ugruntowana wiedza o zachowaniu w górach</b> oraz <b>doświadczenie w wędrówkach górskich</b> i wspinaczce skalnej. Ponadto ważne są następujące predyspozycje:
						</p>
						<ul>
							<li><b>Stabilny krok</b> – Musisz się pewnie poruszać na różnych podłożach, takich jak rumowiska czy lód.</li>
							<li><b>Brak lęku wysokości</b> – Podczas wspinaczki lub przejścia nad rozpadliną skalną jest ważne, żeby zachować zimną krew.</li>
							<li><b>Dobra orientacja w terenie</b> – Podstawowym warunkiem orientacji w nieznanym terenie jest umiejętność czytania map, posługiwania się kompasem, wysokościomierzem i nawigacją GPS.</li>
							<li><b>Bardzo dobra wytrzymałość</b> – Trasa na szczyt górski trwa kilka godzin i wymaga pokonania wielu metrów różnicy wysokości.</li>
							<li><b>Realistyczna ocena własnych możliwości</b> – Aby nie utknąć w górach lub nie przemęczyć się na zbyt wymagającej trasie, powinieneś realistycznie oceniać własną kondycję i sprawność.</li>
						</ul>
						<p>Jeżeli chcesz płynnie i bezpiecznie przejść od wędrówki do wspinaczki lub wyprawy wysokogórskiej, to weź pod uwagę <b>kursy i szkolenia</b> oferowane przez związki turystyki górskiej i lokalnych przewodników wysokogórskich. Nauczysz się na nich, na co powinieneś uważać podczas wspinaczki, poznasz reguły bezpieczeństwa i nauczysz się samodzielnego <b>wspinania na własną odpowiedzialność.</b></p>
						<div id="uwaga">
							<p>
								<img src="static/img/uwaga.svg" alt="uwaga">
								Alpinizm został uznany przez UNESCO za element niematerialnego dziedzictwa kulturowego ludzkości. Uzasadnienie: „Wspinaczka i wędrówki w górach opierają się na różnorodnej wiedzy o przyrodzie i warunkach pogodowych oraz na własnych predyspozycjach fizycznych alpinisty“.
							</p>
						</div>
					</div>
					<div class="tresc">
						<h2 id="planowanie">Planowanie trasy</h2>
						<p>
							Pomysły na wyprawy górskie znajdziesz zarówno w Internecie jak i w aktualnych przewodnikach oraz na mapach. Znajdziesz w nich ważne informacje o długości, profilu wysokościowym i stopniu trudności trasy.
							<br><br>
							Ważne: Realistycznie oceniaj swoją kondycję i umiejętności. Początkujący powinni wyruszyć na pierwszą wyprawę z doświadczonym alpinistą lub przewodnikiem wysokogórskim. Na pierwszą wyprawę dobrze jest wybierać trasy na szczyty nie wyższe niż 2500 metrów.
							<br><br>
							Ponadto początkującym adeptom alpinizmu zaleca się zachowanie szczególnej ostrożności na odcinkach wspinaczkowych. Wspinaczka skalna na dużej wysokości jest trudniejsza niż na przygotowanych bezpiecznych skałkach lub na ściance wspinaczkowej. Dlatego zaczynaj od krótkich fragmentów wspinaczkowych, których trudność jest niższa od twojego maksymalnego poziomu umiejętności na ściance wspinaczkowej. W ten sposób zachowasz pewność, że bez większych trudności dotrzesz do celu, a swoją pierwszą wyprawę zaliczysz do udanych.
							<br><br>
							Na wyprawę zaplanuj zawsze odpowiednio dużo czasu uwzględniając wystarczająco długie przerwy. Jeżeli dla danej trasy nie jest znany przybliżony czas marszu, trzymaj się następujących wskazówek orientacyjnych:
							<br><br>
							Turysta/wspinacz pokonuje <b>w ciągu godziny</b> następujące odległości:
						</p>
						<ul>
							<li>podejście: 300 metrów różnicy wysokości</li>
							<li>zejście: 500 metrów różnicy wysokości</li>
							<li>trasa pozioma: 4 kilometry</li>
						</ul>
						<p>
							Zanim wyruszysz w drogę, zawsze <a href="https://weather.com/?Goto=Redirected" target="_blank">sprawdź prognozę pogody</a>. Pogoda w górach może się szybko zmieniać. Zasięgnij obszernych informacji o trasie: Jak przebiega szlak? Czy i gdzie są skróty? Które rozwidlenia są ważne? Dzięki temu w razie potrzeby będziesz mógł pójść <b>trasą alternatywną.</b>
						</p>
